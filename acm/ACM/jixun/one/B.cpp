#include <bits/stdc++.h>

using namespace std;
int a[1000000000];
int main()
{
    int h, w, n;
    while (cin >> h >> w >> n)
    {
        memset(a, 0, sizeof(a));
        while (n--)
        {
            int k;
            cin >> k;
            int flag = 0;
            for (int i = 1; i <= h; i++)
            {
                if (a[i] + k <= w)
                {
                    a[i] += k;
                    cout << i << endl;
                    flag = 1;
                    break;
                }
            }
            if (flag == 0)
            {
                cout << -1 << endl;
            }
        }
    }
    return 0;
}